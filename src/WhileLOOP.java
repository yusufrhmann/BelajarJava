public class WhileLOOP {
    public static void main(String[] args) {
        int counter = 100;
        while (counter <= 110){
            System.out.println("player " + counter);
            counter+=1;
        }// while di cek dulu, jika benar baru di ulang, jika salah maka stop
    do {
        System.out.println("player " + counter);
    }while (counter <= 110);
    }// do while = di ulang dlu 1x baru di cek, kalau salah, brenti
}
