public class SWITCHstatement {
    public static void main(String[] args) {
        var nilai = "C";
        switch (nilai){
            case "A":
                System.out.println("keren, kamu cumlaude");
                break;
            case "B":
                System.out.println("ciee luluss");
                break;
            case "C":
                System.out.println("akhirnya lulus");
                break;
            case "D":
                System.out.println("MAMPUS NGULANG");
                break;
            default:
                System.out.println("kamu pasti salah jurusan");
        }
    }
}
